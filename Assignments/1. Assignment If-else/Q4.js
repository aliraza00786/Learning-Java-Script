
// Write segment of a program that reads a grade A,B,C,D or F and then prints Excellent, Good, Fair, Poor or Failure depending on the grade. Use multiple if statement?

 let Grade = prompt('Enter the Grade');

 if (Grade === 'A'){
    console.log('Excellent');
 }

else if (Grade === 'B'){
    console.log('Good');
 }

 else if (Grade === 'C'){
     console.log('Fair');
 }

else if (Grade === 'D'){
     console.log('Poor');
 }

 else if (Grade === 'F'){
    console.log('Failure');
 }
 

